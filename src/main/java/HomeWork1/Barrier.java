package HomeWork1;

public class Barrier {
    private String name;
    private int high;
    private int lenght;

    public Barrier (String name) {
        this.name = name;

    }

    public int getHigh() {
        return high;
    }

    public int getLenght() {
        return lenght;
    }

    public String getName() {
        return name;
    }

}
